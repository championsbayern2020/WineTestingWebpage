    x[4][1] = {0, 0, 0, 0, 0, 0};   // 可以接受，但要在可控范围内
    x[4][2] = {-1, 1, -1, 0, 0, -1}; // 不太喜欢，更喜欢稳定
    x[4][3] = {-1, 1, -1, 1, -1, -1}; // 完全不喜欢，认为风险太大
    // 问题六：你在社交场合通常是如何表现的？
